package com.student.pojo;

public class pdf {
    private String number;
    private String name;
    private String grade;
    private String phone;
    private String age;
    private String gender;
    private String self;
    private String ability;
    private String avatar_img;
    private String life1_img;
    private String life2_img;
    private String life3_img;

  public pdf() {
  }

  public pdf(String number, String name, String grade, String phone, String age, String gender, String self, String ability, String avatar_img, String life1_img, String life2_img, String life3_img) {
    this.number = number;
    this.name = name;
    this.grade = grade;
    this.phone = phone;
    this.age = age;
    this.gender = gender;
    this.self = self;
    this.ability = ability;
    this.avatar_img = avatar_img;
    this.life1_img = life1_img;
    this.life2_img = life2_img;
    this.life3_img = life3_img;
  }

  public String getNumber() {
    return number;
  }

  public void setNumber(String number) {
    this.number = number;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getGrade() {
    return grade;
  }

  public void setGrade(String grade) {
    this.grade = grade;
  }

  public String getPhone() {
    return phone;
  }

  public void setPhone(String phone) {
    this.phone = phone;
  }

  public String getAge() {
    return age;
  }

  public void setAge(String age) {
    this.age = age;
  }

  public String getGender() {
    return gender;
  }

  public void setGender(String gender) {
    this.gender = gender;
  }

  public String getSelf() {
    return self;
  }

  public void setSelf(String self) {
    this.self = self;
  }

  public String getAbility() {
    return ability;
  }

  public void setAbility(String ability) {
    this.ability = ability;
  }

  public String getAvatar_img() {
    return avatar_img;
  }

  public void setAvatar_img(String avatar_img) {
    this.avatar_img = avatar_img;
  }

  public String getLife1_img() {
    return life1_img;
  }

  public void setLife1_img(String life1_img) {
    this.life1_img = life1_img;
  }

  public String getLife2_img() {
    return life2_img;
  }

  public void setLife2_img(String life2_img) {
    this.life2_img = life2_img;
  }

  public String getLife3_img() {
    return life3_img;
  }

  public void setLife3_img(String life3_img) {
    this.life3_img = life3_img;
  }
}
